// Generates the WA postcode->suburb dataset used for signup/profile validation and the
// catchment report. Emits TWO identical-data files (frontend + convex) from ONE source so
// they can't drift (SPEC_PROFILE_POSTCODE_SUBURB.md).
//
// Usage:  node scripts/gen-wa-postcodes.cjs <path-to-australian_postcodes.csv>
// Source dataset: https://raw.githubusercontent.com/matthewproctor/australianpostcodes/master/australian_postcodes.csv
//
// Filter: state === "WA" AND postcode 6000-6797 (geographic delivery range). Excludes the
// dedicated PO-box / LVR ranges (6800-6999) and the Indian Ocean territories (6798/6799),
// satisfying "real delivery suburbs only" (decision #3).

const fs = require('fs')
const path = require('path')

const csvPath = process.argv[2]
if (!csvPath) { console.error('Usage: node gen-wa-postcodes.cjs <csv>'); process.exit(1) }

const raw = fs.readFileSync(csvPath, 'utf8')
const lines = raw.split(/\r?\n/)

function titleCase(s) {
  return s.toLowerCase().replace(/\b([a-z])/g, (m) => m.toUpperCase())
}

const map = {} // postcode -> Set<suburb>
for (let i = 1; i < lines.length; i++) {
  const line = lines[i]
  if (!line) continue
  const toks = [...line.matchAll(/"((?:[^"]|"")*)"/g)].map((m) => m[1].replace(/""/g, '"'))
  if (toks.length < 4) continue
  const postcode = toks[1]
  const locality = toks[2]
  const state = toks[3]
  if (state !== 'WA') continue
  if (!/^6\d{3}$/.test(postcode)) continue
  const n = parseInt(postcode, 10)
  if (n < 6000 || n > 6797) continue
  if (!locality || !locality.trim()) continue
  // Drop non-residential delivery artifacts (GPO, delivery/mail/business centres, PO boxes).
  if (/\b(gpo|po box|po boxes|lvr|delivery centre|delivery center|mail centre|mail center|business centre|business center|post shop|msc)\b/i.test(locality)) continue
  ;(map[postcode] = map[postcode] || new Set()).add(titleCase(locality.trim()))
}

const postcodes = Object.keys(map).sort()
const obj = {}
let localityCount = 0
for (const pc of postcodes) {
  const arr = [...map[pc]].sort((a, b) => a.localeCompare(b))
  obj[pc] = arr
  localityCount += arr.length
}

const header = `// AUTO-GENERATED by scripts/gen-wa-postcodes.cjs — do not edit by hand.
// Source: matthewproctor/australianpostcodes (filtered: state=WA, postcode 6000-6797 = real
// delivery suburbs; PO-box/LVR 6800-6999 and external territories excluded).
// ${postcodes.length} postcodes, ${localityCount} localities.
// Single source of truth for postcode->suburb lookup + WA postcode validation.
`

const helpers = `
export const WA_POSTCODES: Readonly<Record<string, readonly string[]>> =
  ${JSON.stringify(obj)} as Readonly<Record<string, readonly string[]>>

/** Suburbs for a postcode (sorted, de-duplicated). [] if unknown / not a valid WA delivery postcode. */
export function suburbsForPostcode(pc: string): readonly string[] {
  return WA_POSTCODES[(pc ?? '').trim()] ?? []
}

/** True iff a 4-digit WA postcode (6000-6797) that maps to >=1 real suburb. */
export function isValidWaPostcode(pc: string): boolean {
  const p = (pc ?? '').trim()
  if (!/^6\\d{3}$/.test(p)) return false
  return (WA_POSTCODES[p]?.length ?? 0) > 0
}

/** True iff the suburb belongs to that postcode (case-insensitive). */
export function isValidPair(pc: string, suburb: string): boolean {
  const s = (suburb ?? '').trim().toLowerCase()
  if (!s) return false
  return suburbsForPostcode(pc).some((x) => x.toLowerCase() === s)
}
`

const content = header + helpers
const repoRoot = path.resolve(__dirname, '..')
const targets = [
  path.join(repoRoot, 'src', 'lib', 'wa-postcodes.ts'),
  path.join(repoRoot, 'convex', 'lib', 'waPostcodes.ts'),
]
for (const t of targets) {
  fs.writeFileSync(t, content)
  console.log('wrote', t)
}
console.log(`postcodes=${postcodes.length} localities=${localityCount} bytes=${content.length}`)
